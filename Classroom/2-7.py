name = "\tBui Ngoc Thien    \n"
print(name)
# lstrip() tra ve xau ban dau sau khi xoa cac ky tu o phan dau (mac dinh la khoang trang)
a = name.lstrip()
# rstrip() tra ve xau ban dau sau khi xoa cac ky tu o phia sau (mac dinh la khoang trang)
b = name.rstrip()
# strip() xoa ca phia truoc lan phia sau
c = name.strip()
print(a)
print(b)
print(c)
